idade1 = int(input('Informe a primeira idade para a comparação: '))
idade2 = int(input('Informe a segunda idade para a comparação: '))

if idade1 > idade2:
    print ('Primeira idade é maior que a segunda idade informada')
elif idade1 < idade2:
    print ('A segunda idade é maior que a primeira idade informada')
elif idade1 == idade2:
    print('As idades informadas são iguais')    
