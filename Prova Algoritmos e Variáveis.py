#calculando a Area de um retângulo
base = float(input('Insira a base do triângulo a ser calculado:'))
print (f'A base enviada foi: {base}')
altura = float(input('Insira a Altura do triângulo a ser calculado'))
print (f'A altura enviada foi:{altura}')

area = base * altura
print (f'A Area do Triangulo calculada é de: {area}')
