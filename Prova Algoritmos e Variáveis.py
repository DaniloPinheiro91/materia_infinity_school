#calculando a Area de um retângulo

base = float(input('Insira a base do retângulo a ser calculado:'))
print (f'A base enviada foi: {base}')
altura = float(input('Insira a Altura do retângulo a ser calculado'))
print (f'A altura enviada foi:{altura}')

#Calculo da Area

area = base * altura
print (f'A Area do retângulo calculada é de: {area}')
